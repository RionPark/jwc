package d20200624.최인혁;

public class Knife {
	String category;
	String purpose;
	

	


	public Knife(String category, String purpose) {
		this.category = category;
		this.purpose = purpose;
		
	}


	@Override
	public String toString() {
		return "Knife [category=" + category + ", purpose=" + purpose + "]";
	}




	}
